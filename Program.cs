namespace square
{
    internal class Program
    {
        static void Main(string[] args)
        {
            int count = 5;
            static void Main(string[] args)
            {
                int count = 5;
                for (int i = 1; i <= count; i++)
                {

                    for (int k = 1; k <= count - 1; k++)
                    {
                        Console.Write(" ");

                    }
                    for (int j = 1; j <= count; j++)
                    {
                        Console.Write("* ");
                    }
                    Console.WriteLine();
                }



            }
        }
    }
